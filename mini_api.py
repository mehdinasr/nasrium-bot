@app.route('/api/wallet/transfer', methods=['POST'])
def internal_transfer():
    try:
        data = request.json; sender_uid = data.get('user_id'); receiver_uid = data.get('receiver_id')
        currency = data.get('currency'); amount = float(data.get('amount', 0))
        if not sender_uid or not receiver_uid: return jsonify({'error': 'Missing IDs'}), 400

        sender = players_collection.find_one({'user_id': sender_uid})
        if not sender: return jsonify({'error': 'Sender not found'}), 404

        receiver = players_collection.find_one({'user_id': receiver_uid})
        if not receiver: return jsonify({'error': 'Receiver not found'}), 404

        result = TransferEngine.initiate_transfer(sender, receiver_uid, currency, amount)
        
        if result['success']:
            # کسر از فرستنده (موجودی NSM + موجودی TON کارمزد)
            update_sender = {
                '$inc': {currency: -amount, 'ton_balance': -result['fee_ton']}
            }
            # اضافه کردن به گیرنده
            update_receiver = {
                '$inc': {currency: amount}
            }
            # کارمزد TON به فروشگاه/پروه واریز میشود (در اینجا از سیستم کسر نمیشود فقط از کاربر کسر میشود)
            
            players_collection.update_one({'user_id': sender_uid}, update_sender)
            players_collection.update_one({'user_id': receiver_uid}, update_receiver)
            
            NotificationEngine.add_notification(sender_uid, f'💸 You sent {amount} {currency} to ID {receiver_uid}. Fee: {result["fee_ton"]} TON.', 'transfer', players_collection)
            NotificationEngine.add_notification(receiver_uid, f'💰 You received {amount} {currency} from ID {sender_uid}.', 'transfer', players_collection)
            return jsonify(result)
        else: return jsonify(result), 400
    except Exception as e: return jsonify({'error': str(e)}), 500


