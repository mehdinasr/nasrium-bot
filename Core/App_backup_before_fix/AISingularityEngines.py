class CognitiveMiningEngine:
    """سیستم استخراج منابع بر اساس تعامل هوشمند کاربر."""
    @staticmethod
    def calculate_cognitive_yield(session_duration, interaction_score):
        return (session_duration / 60) * interaction_score

class SovereigntyIndex:
    """شاخص اعتبار و قدرت نهایی شهروند در تمدن."""
    @staticmethod
    def get_index(ixp, honor, credit):
        return (ixp * 0.4) + (honor * 0.3) + (credit * 0.3)
